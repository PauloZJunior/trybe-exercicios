let balance = 3275;
function sumBalance(value) {
    return value + balance;
}
function subtractBalance(value) {
    return balance - value;
}
function multiplytBalance(value) {
    return balance * value;
}
function divideBalance(value) {
    return balance / value;
}
console.log(sumBalance(685));
console.log(subtractBalance(213));
console.log(multiplytBalance(3));
console.log(divideBalance(2));
